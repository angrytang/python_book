#-*- coding:UTF-8 -*-

i = int(input("请输入当月利润："))

if i <= 100000:
    j = i * 0.1
elif i <= 200000:
    j = 10000 + (i - 100000) * 0.075
elif i <= 400000:
    j = 10000 + 7500 + (i - 200000) * 0.05
elif i <= 600000:
    j = 10000 + 7500 + 10000 + (i - 400000) * 0.03
elif i <= 1000000:
    j = 10000 + 7500 + 10000 + 6000 + (i - 600000) * 0.015
else:
    j = 10000 + 7500 + 10000 + 6000 + 6000 + (i - 1000000) * 0.01

print("应发放奖金为：",j)